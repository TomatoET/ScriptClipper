_language = DEFAULT_LANGUAGE


def set_language(language: str) -> None:
    global _language
    _language = language if language in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def current_language() -> str:
    return _language


def t(key: str, **kwargs: object) -> str:
    text = TRANSLATIONS.get(_language, TRANSLATIONS[DEFAULT_LANGUAGE]).get(key)
    if text is None:
        text = TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key)
    if kwargs:
        return text.format(**kwargs)
    return text


def roll_display_name(roll_type: str, tab: bool = False) -> str:
    key = "b_roll" if roll_type in {"b_roll", "b-roll", "B-Roll"} else "a_roll"
    if tab:
        key = "b_tab" if key == "b_roll" else "a_tab"
    return ROLL_LABELS.get(_language, ROLL_LABELS[DEFAULT_LANGUAGE])[key]
